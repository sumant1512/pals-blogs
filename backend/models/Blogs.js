const mongoose = require("mongoose");

const BlogsSchema = new mongoose.Schema({
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: "user",
  },
  title: {
    type: String,
    required: true,
  },
  subTitle: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
  },
});

const Blogs = mongoose.model("blogs", BlogsSchema);
Blogs.createIndexes();
module.exports = Blogs;
